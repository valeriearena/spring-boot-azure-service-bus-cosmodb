package com.banyan.poc.moduleB.controllers;

import com.banyan.poc.moduleB.controller.ModuleBRestController;
import com.banyan.poc.moduleB.service.MessageService;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.web.servlet.WebMvcTest;
import org.springframework.boot.test.mock.mockito.MockBean;
import org.springframework.test.context.ActiveProfiles;
import org.springframework.test.context.junit.jupiter.SpringExtension;
import org.springframework.test.web.servlet.MockMvc;

import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.post;
import static org.springframework.test.web.servlet.result.MockMvcResultHandlers.print;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.jsonPath;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

@ExtendWith(SpringExtension.class)
@WebMvcTest(ModuleBRestController.class)
@ActiveProfiles("local")
public class ModuleBRestControllerUnitTests {

	@Autowired
	private MockMvc mockMvc;

	@MockBean
	private MessageService messageService;

	@Test
	public void testMessage() throws Exception {

		this.mockMvc
				.perform(post("/message"))
				.andDo(print()).andExpect(status().isOk())
				.andExpect(jsonPath("$.backToTheFutureCharacter").exists());
	}

}
